void calculateTemperature(void) {
	float steinhart;
	if (HAL_ADC_Start(&hadc1) != HAL_OK) {
		printf("HAL_ADC_Start Error. \r\n");
	}

	if (HAL_ADC_PollForConversion(&hadc1, 10) != HAL_OK) {
		printf("HAL_ADC_PollForConversion Error. \r\n");
	} else {
		/* ADC conversion completed */

		uhADCxConvertedValue = HAL_ADC_GetValue(&hadc1);
		printf("Analog value: %4.2f\t", uhADCxConvertedValue);

		uhADCxConvertedValue = (4095.0 / uhADCxConvertedValue) - 1;
		uhADCxConvertedValue = SERIESRESISTOR / uhADCxConvertedValue; // 10K / (1023/ADC - 1)

		printf("Thermistor resistance: %4.2f\r\n", uhADCxConvertedValue);

		steinhart = uhADCxConvertedValue / THERMISTORNOMINAL;     // (R/Ro)
		steinhart = log(steinhart);                  // ln(R/Ro)
		steinhart /= BCOEFFICIENT;                   // 1/B * ln(R/Ro)
		steinhart += 1.0 / (TEMPERATURENOMINAL + 273.15); // + (1/To)
		steinhart = 1.0 / steinhart;                 // Invert
		steinhart -= 273.15;                         // convert to C

		printf("Temperature: %4.2f *C\r\n", steinhart);

	}

	if (steinhart >= 27.5) {
		//peltier ON Fan ON
		HD44780_GotoXY(0, 1);
		HD44780_PutStr("COOLING ON ");
		HAL_GPIO_WritePin(GPIOA, Peltier_Relay_Pin, GPIO_PIN_SET); //On
		HAL_GPIO_WritePin(GPIOA, Motor_Relay_Pin, GPIO_PIN_SET);

	} else if (steinhart <= 25.5) {
		HD44780_GotoXY(0, 1);
		HD44780_PutStr("COOLING OFF");
		HAL_GPIO_WritePin(GPIOA, Motor_Relay_Pin, GPIO_PIN_RESET); //Off
		HAL_GPIO_WritePin(GPIOA, Peltier_Relay_Pin, GPIO_PIN_RESET);

	}

	HD44780_GotoXY(0, 0);
	sprintf(temp, "Temp: %0.1f 'C", steinhart);
	HD44780_PutStr(temp);
	HD44780_PutStr(" ");

	printf("\r\n");
	HAL_Delay(1000);

}
